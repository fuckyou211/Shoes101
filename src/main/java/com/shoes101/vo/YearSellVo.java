package com.shoes101.vo;

/**
 * 每年销量
 */
public class YearSellVo {

    private String year;
    private int sells;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getSells() {
        return sells;
    }

    public void setSells(int sells) {
        this.sells = sells;
    }
}
